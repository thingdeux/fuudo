import SwiftUI

extension Color {
    static let primaryColor: Color = Color("PrimaryColor")
    static let accentColor: Color = Color("AccentColor")
    static let badgeColor: Color = Color("BadgeColor")
    static let primaryTextColor: Color = Color("PrimaryTextColor")
    
    static let accentedBackgroundColor: Color = Color("AccentedBackgroundColor")
    static let lightBackgroundColor: Color = Color("LightBackgroundColor")
    static let boldOrangeAccentColor: Color = Color("BoldOrangeAccentColor")
    
    static let lightPrimaryColor: Color = Color("LightPrimaryColor")    
}

extension Font {
